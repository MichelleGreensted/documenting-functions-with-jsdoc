/**
 * When given a number, returns the negative version of the number.
 * @param {number} n - input number
 * @returns {number} negative version of n
 */
function asNegative(n) {
  n2 = n * -1
  return n2
}

console.log(asNegative(4))
console.log(asNegative(-3))
console.log(asNegative(0))
